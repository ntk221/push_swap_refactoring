#include "libft.h"

char	*ft_strdup(char const *src)
{
	char	*new;
	size_t	len;
	size_t	i;

	len = ft_strlen(src);
	new = (char *)malloc(sizeof((*src)) * (len + 1));
	if (!new)
		return (NULL);
	i = 0;
	while (src[i])
	{
		new[i] = src[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
